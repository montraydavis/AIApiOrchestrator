/**
 * Represents the supported data types for API parameters.
 *
 * @typedef {'string' | 'number' | 'boolean' | 'object' | 'array'} ParameterType
 * @property string - A string value.
 * @property number - A numeric value.
 * @property boolean - A boolean value (true or false).
 * @property object - An object value (key-value pairs).
 * @property array - An array of values.
 */
export type ParameterType = 'string' | 'number' | 'boolean' | 'object' | 'array';